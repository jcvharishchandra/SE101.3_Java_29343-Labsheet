package com.mycompany.javapractical04;

public class Person
{
    private String name;
    private int id;
    
    public void setName(String a)
    {
        name = a;
    }
    
    public String getName()
    {
        return name;
    }
    
    public void setId(int b)
    {
        id = b;
    }
    
    public int getId()
    {
        return id;
    }
}
