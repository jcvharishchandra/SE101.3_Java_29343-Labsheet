package com.mycompany.javapractical04;

public class SuperB
{
    public int x;
    
    public void setIt(int n)
    { 
        x=n;
    }
   
    public void increase()
    { 
        x=x+1;
    }
    
    public void triple() 
    {
        x=x*3;
    }
    
    public int returnIt() 
    {
        return x;
    }

}
